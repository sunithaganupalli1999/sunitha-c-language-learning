//c program to print integer data types
#include<stdio.h>
int main()
{
    //integer value with positive data
    int a=9;
    //integer value withn negative data
    int b=-9;
    //u or u is used for unsigned int in c
    int c=89u;
    //l or 1 is used for long int in c
    long int d=99998l;
    printf(" integer value with positive data:%d\n",a);
    printf("integer value with negative data:%d\n",b);
    printf("integer value with an unsigned intdata:%d\n",c);
    printf("integer value with an long int data:%1d",d);
    return 0;
}